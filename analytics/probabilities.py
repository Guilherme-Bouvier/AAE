def probability_high(volatility, streak):

    score = 0

    # ======================================================
    # ANALISA VOLATILIDADE
    # ======================================================

    if volatility > 2:
        score += 2

    # ======================================================
    # ANALISA STREAK
    # ======================================================

    if streak > 5:
        score += 1

    # ======================================================
    # NORMALIZA SCORE
    # ======================================================

    probability = min(score / 10, 0.95)

    return probability